package cbrmodel;

public class Tip {
	
	private int id;
	private String type;
	public Tip() {
		super();
	}
	public Tip(int id, String type) {
		super();
		this.id = id;
		this.type = type;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
	

}
